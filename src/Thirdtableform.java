    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        try {
            // TODO add your handling code here:
            Connection con=getConnection();
            double totalans=0;
            double prodrate = 0;
            String sqlprod="select productrate from product where productid='"+pid.getText()+"'";
            Statement st=con.createStatement();
            ResultSet rs1=st.executeQuery(sqlprod);
            while(rs1.next())
            {
                prodrate= rs1.getDouble("productrate");
                
            }
            String quant=quantity.getText();
            double quant1=Double.parseDouble(quant);
            totalans=quant1*prodrate;
            
            rs1.close();
            st.close();
            //String sql="insert into qtypurch(qty,corderid,productid,total) values('"+quantity.getText()+"','"+cid.getText()+"','"+pid.getText()+"','"+totalans+"')";
            String sql="insert into qtypurch(qty,corderid,productid,total) values (?,?,?,?)";
           // Statement st2=con.createStatement();
            //ResultSet rs2=st2.executeUpdate(sql);
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setString(1, quantity.getText());
            pst.setString(2, cid.getText());
            pst.setString(3,pid.getText());
            //pst.setString(4,"totalans");
            pst.setDouble(4, totalans);
            pst.execute();
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            Show_Users_In_JTable();
            JOptionPane.showMessageDialog(null, "Data inserted");
        } catch (SQLException ex) {
            Logger.getLogger(Thirdtableform.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addActionPerformed

