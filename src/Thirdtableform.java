    public ArrayList<Third> getqtyList() throws SQLException
    {
        ArrayList<Third> qtylist=new ArrayList<Third>();
        Connection con=getConnection();
        
        String query="Select * from qtypurch";
        Statement st;
        ResultSet rs;
        
        try{
            
            st=con.createStatement();
            rs=st.executeQuery(query);
            
            Third th;
            
            while(rs.next())
            {
                th=new Third(rs.getInt("qty"),rs.getInt("corderid"),rs.getInt("productid"),rs.getDouble("total"));
                qtylist.add(th);
            }
            
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        return qtylist;
    }
    
